SELECT Nume,Prenume,Adresa
FROM INTREPRINDERE.dbo.CLIENTI
WHERE Adresa='Bucuresti'
UNION
SELECT Nume,Prenume,Adresa
FROM INTREPRINDERE.dbo.FURNIZOR
WHERE Adresa = 'Bucuresti'

--3.2
SELECT *
FROM INTREPRINDERE.dbo.ANGAJATI
CROSS JOIN INTREPRINDERE.dbo.SECTII

--3.3
SELECT Nume,Prenume,NrComponente
FROM INTREPRINDERE.dbo.FURNIZOR
INNER JOIN INTREPRINDERE.dbo.ACHIZITII
ON ACHIZITII.IdFurnizor = FURNIZOR.IdFurnizor
WHERE ACHIZITII.NrComponente > 200 OR ACHIZITII.NrComponente = 200;

--3.4
SELECT Nume,Prenume,Denumire
FROM INTREPRINDERE.dbo.ACHIZITII
INNER JOIN INTREPRINDERE.dbo.ANGAJATI
ON ACHIZITII.IdAchizitie = ANGAJATI.IdAngajat
INNER JOIN INTREPRINDERE.dbo.COMPONENTE
ON ACHIZITII.IdComponenta = COMPONENTE.IdComponenta
WHERE COMPONENTE.Denumire = 'Condensator';


--3.5
SELECT Nume,Prenume,Adresa
FROM INTREPRINDERE.dbo.ACHIZITII
INNER JOIN INTREPRINDERE.dbo.FURNIZOR
ON ACHIZITII.IdFurnizor = FURNIZOR.IdFurnizor
INNER JOIN INTREPRINDERE.dbo.COMPONENTE
ON ACHIZITII.IdComponenta = COMPONENTE.IdComponenta
WHERE COMPONENTE.Denumire = 'Rezistenta';

--3.6
SELECT Nume,Prenume,Adresa
FROM INTREPRINDERE.dbo.FURNIZOR
INNER JOIN INTREPRINDERE.dbo.ACHIZITII
ON ACHIZITII.IdFurnizor = FURNIZOR.IdFurnizor
INNER JOIN INTREPRINDERE.dbo.COMPONENTE
ON ACHIZITII.IdComponenta = COMPONENTE.IdComponenta
WHERE COMPONENTE.Denumire = 'Condensator' AND (ACHIZITII.NrComponente > 150 OR ACHIZITII.NrComponente = 150);


--3.7
SELECT Nume,Prenume,Adresa
FROM INTREPRINDERE.dbo.VANZARI
INNER JOIN INTREPRINDERE.dbo.PRODUSE
ON VANZARI.IdProdus = PRODUSE.IdProdus
INNER JOIN INTREPRINDERE.dbo.CLIENTI
ON VANZARI.IdClient = CLIENTI.IdClient
WHERE PRODUSE.Denumire = 'Monitor' AND (VANZARI.NrProduse > 10 OR VANZARI.NrProduse = 10)


--3.8
SELECT Nume,Prenume
FROM INTREPRINDERE.dbo.VANZARI
INNER JOIN INTREPRINDERE.dbo.PRODUSE
ON VANZARI.IdProdus = PRODUSE.IdProdus
INNER JOIN INTREPRINDERE.dbo.ANGAJATI
ON VANZARI.IdVanzator = ANGAJATI.IdAngajat
WHERE PRODUSE.Denumire = 'Monitor';


--3.9
SELECT ANGAJATI.Nume,ANGAJATI.Prenume,ANGAJATI.Data_Nasetii
FROM INTREPRINDERE.dbo.ACTIVITATI
INNER JOIN INTREPRINDERE.dbo.ANGAJATI
ON ACTIVITATI.IdAngajat = ANGAJATI.IdAngajat
INNER JOIN INTREPRINDERE.dbo.PROIECTE
ON PROIECTE.IdProiect = ACTIVITATI.IdProiect
WHERE PROIECTE.Denumire = 'Sistem de achizitii de date'

--3.10

SELECT SECTII.Nume
FROM INTREPRINDERE.dbo.ACTIVITATI
INNER JOIN INTREPRINDERE.dbo.ANGAJATI
ON ACTIVITATI.IdAngajat = ANGAJATI.IdAngajat
INNER JOIN INTREPRINDERE.dbo.SECTII
ON SECTII.IdSectie = ANGAJATI.IdSectie
INNER JOIN INTREPRINDERE.dbo.PROIECTE
ON PROIECTE.IdProiect = ACTIVITATI.IdProiect
WHERE PROIECTE.Denumire = 'Sistem de achizitii de date'

--3.11
SELECT ANGAJATI.Nume, ANGAJATI.Prenume,ANGAJATI.Data_Nasetii
FROM INTREPRINDERE.dbo.ANGAJATI
INNER JOIN INTREPRINDERE.dbo.INGINERI
ON INGINERI.IdAngajat = ANGAJATI.IdAngajat
WHERE INGINERI.Specialitatea ='Electronica'

--3.12
