FROM INTREPRINDERE.dbo.ANGAJATI
INNER JOIN INTREPRINDERE.dbo.SECTII
ON ANGAJATI.IdSectie = SECTII.IdSectie
WHERE SECTII.Nume = 'Productie' AND ANGAJATI.Prenume = 'Ion'

--3.13
SELECT ANGAJATI.Nume, ANGAJATI.Prenume
FROM INTREPRINDERE.dbo.ANGAJATI
INNER JOIN INTREPRINDERE.dbo.SECTII
ON ANGAJATI.IdSectie = SECTII.IdSectie
WHERE SECTII.Nume = 'Productie'


--3.14
SELECT ANGAJATI.Nume,ANGAJATI.Prenume
FROM INTREPRINDERE.dbo.ACHIZITII
INNER JOIN  INTREPRINDERE.dbo.ANGAJATI
ON ACHIZITII.IdAchizitor  = ANGAJATI.IdAngajat
INNER JOIN INTREPRINDERE.dbo.FURNIZOR
ON FURNIZOR.IdFurnizor = ACHIZITII.IdFurnizor
WHERE FURNIZOR.Nume = 'Popescu' AND FURNIZOR.Prenume = 'Razvan'

--3.15
SELECT ANGAJATI.Nume,ANGAJATI.Prenume
FROM INTREPRINDERE.dbo.VANZARI
INNER JOIN INTREPRINDERE.dbo.CLIENTI
ON CLIENTI.IdClient = VANZARI.IdClient
INNER JOIN INTREPRINDERE.dbo.ANGAJATI
ON ANGAJATI.IdAngajat = VANZARI.IdVanzator
WHERE CLIENTI.Nume='Marinescu' AND CLIENTI.Prenume='Ion';

--3.16
SELECT PRODUSE.Denumire
FROM INTREPRINDERE.dbo.COMPONENTE
INNER JOIN INTREPRINDERE.dbo.COMPOZITIE
ON COMPONENTE.IdComponenta = COMPOZITIE.IdComponenta
INNER JOIN INTREPRINDERE.dbo.PRODUSE
ON PRODUSE.IdProdus = COMPOZITIE.IdProdus
INNER JOIN INTREPRINDERE.dbo.ACHIZITII
ON ACHIZITII.IdComponenta = COMPONENTE.IdComponenta
INNER JOIN INTREPRINDERE.dbo.FURNIZOR
ON ACHIZITII.IdFurnizor = FURNIZOR.IdFurnizor
WHERE FURNIZOR.Nume = 'Danescu' AND FURNIZOR.Prenume = 'Ovidiu'

--3.17
